#include "myMath.h"

float add(float x , float y)
{
    float sum = x+y;
    return sum;
}

float sub(float x , float y)
{   
    float difference = x-y;
    return difference;
}

double mul(double x , int y)
{
    double product = y*x;
    return product;
}

double div(double x, int y)
{
    double quotient = x/y;
    return quotient;
}


